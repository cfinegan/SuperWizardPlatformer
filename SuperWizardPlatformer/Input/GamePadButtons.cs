namespace SuperWizardPlatformer.Input
{
    /// <summary>
    /// The set of virtual  buttons that can are recognized by the game, and should be mappable
    /// to any reasonable game pad.
    /// </summary>
    enum GamePadButtons
    {
        None = 0,
        A,
        B,
        X,
        Y,
        Start,
        Select,
        L,
        R,
        Up,
        Down,
        Left,
        Right
    }
}
